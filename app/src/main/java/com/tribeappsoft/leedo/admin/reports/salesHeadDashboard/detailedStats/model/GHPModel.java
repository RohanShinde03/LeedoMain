package com.tribeappsoft.leedo.admin.reports.salesHeadDashboard.detailedStats.model;

public class GHPModel {

    private String ghp_date;
    private String ghp_no;
    private String ghp_amount;
    private String project_name;

    public String getGhp_date() {
        return ghp_date;
    }

    public void setGhp_date(String ghp_date) {
        this.ghp_date = ghp_date;
    }

    public String getGhp_no() {
        return ghp_no;
    }

    public void setGhp_no(String ghp_no) {
        this.ghp_no = ghp_no;
    }

    public String getGhp_amount() {
        return ghp_amount;
    }

    public void setGhp_amount(String ghp_amount) {
        this.ghp_amount = ghp_amount;
    }

    public String getProject_name() {
        return project_name;
    }

    public void setProject_name(String project_name) {
        this.project_name = project_name;
    }
}
